#include <iostream>

void multiline_comment()
{
    // inline comment can alse used to comment multiline,
    // comment four lines so that this function will only print one line
    std::cout << "Hello World!" << std::endl;
    std::cout << "Hello World!" << std::endl;
    std::cout << "Hello World!" << std::endl;
    std::cout << "Hello World!" << std::endl;
    std::cout << "Hello World!" << std::endl;
}